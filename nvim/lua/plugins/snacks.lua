return {
  "folke/snacks.nvim",
  config = function(_, opts)
    require("snacks").setup(opts)
    Snacks.toggle
      .new({
        id = "transparency_toggle",
        name = "Transparent Background",
        get = function()
          if vim.g.neovide then
            return vim.g.neovide_opacity ~= 1
          else
            local bg = vim.api.nvim_get_hl(0, { name = "Normal" }).bg
            return bg == nil
          end
        end,
        set = function(_)
          if vim.g.neovide then
            if vim.g.neovide_opacity == 1 then
              vim.g.neovide_opacity = vim.g.neovide_transparent_opacity
              vim.g.neovide_normal_opacity = vim.g.neovide_transparent_opacity
              vim.g.neovide_window_blurred = true
            else
              vim.g.neovide_opacity = 1
              vim.g.neovide_normal_opacity = 1
              vim.g.neovide_window_blurred = false
            end
          else
            vim.cmd("TransparentToggle")
          end
        end,
      })
      :map("<leader>ut")
  end,
  opts = {
    -- Disable explorer in favor of yazi
    explorer = {
      enabled = false,
    },
    dashboard = {
      preset = {
        header = header,
      },
    },
    picker = {
      sources = {
        explorer = {
          follow_file = false,
        },
        keymaps = {
          layout = function()
            return {
              preset = "vertical",
              layout = {
                width = 0.8,
              },
            }
          end,
        },
        notifications = {
          win = {
            preview = {
              wo = {
                wrap = true,
              },
            },
          },
        },
        colorschemes = {
          layout = function()
            return {
              hidden = {},
              preset = "vscode",
            }
          end,
        },
      },
    },
  },
}
